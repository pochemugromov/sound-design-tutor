def build_title_prompt(message: str, has_images: bool = False) -> str:
    text = (message or "").strip()
    parts = []
    if text:
        parts.append(f"Сообщение студента:\n{text}")
    if has_images:
        parts.append("К сообщению прикреплено изображение.")
    parts.append("Придумай короткое название чата по правилам.")
    return "\n\n".join(parts)


