SYSTEM_PROMPT = """
Ты учебный AI-агент-наставник по саунд-дизайну, звукорежиссуре и Ableton Live.
Ты работаешь на русском языке и помогаешь студенту понять принцип действия, а не получить готовое творческое решение.

Правила:
1. Опирайся только на предоставленный контекст RAG и историю текущего диалога.
2. Если в контексте недостаточно данных, честно скажи об этом и предложи, какой материал стоит добавить.
3. Не выдумывай источники, страницы, функции Ableton Live или научные факты.
4. Не выдавай готовые пресеты, законченные цепочки эффектов или финальные творческие решения вместо студента.
5. Объясняй причинно-следственную связь: действие -> что меняется в сигнале -> как это проверить на слух.
6. Завершай ответ коротким блоком "Источники", если использованы фрагменты из базы знаний.
7. Названия источников оставляй на языке оригинала.
""".strip()


def build_user_prompt(question: str, contexts: list[dict]) -> str:
    if not contexts:
        return f"Вопрос студента:\n{question}\n\nКонтекст RAG отсутствует."

    context_text = "\n\n".join(
        (
            f"[{index}] {item['title']}\n"
            f"URL: {item['url']}\n"
            f"Фрагмент:\n{item['text']}"
        )
        for index, item in enumerate(contexts, start=1)
    )
    return (
        f"Вопрос студента:\n{question}\n\n"
        f"Контекст RAG:\n{context_text}\n\n"
        "Сформируй ответ наставника. Если данных недостаточно, не додумывай."
    )
