def build_user_prompt(question: str, contexts: list[dict], rag_query: str | None = None) -> str:
    question_block = f"Вопрос студента:\n{question}"
    if rag_query and rag_query.strip() != question.strip():
        question_block += f"\n\nПоисковый контекст диалога для RAG:\n{rag_query}"

    if not contexts:
        return f"{question_block}\n\nКонтекст RAG отсутствует."

    context_blocks = []
    for index, item in enumerate(contexts, start=1):
        page_number = item.get("page_number")
        page_line = f"Page: {page_number}\n" if page_number else ""
        context_blocks.append(
            f"[{index}] {item['title']}\n"
            f"URL: {item['url']}\n"
            f"{page_line}"
            f"Фрагмент:\n{item['text']}"
        )
    context_text = "\n\n".join(context_blocks)
    return (
        f"{question_block}\n\n"
        f"Контекст RAG:\n{context_text}\n\n"
        "Сформируй ответ наставника. Если данных недостаточно, не додумывай."
    )
